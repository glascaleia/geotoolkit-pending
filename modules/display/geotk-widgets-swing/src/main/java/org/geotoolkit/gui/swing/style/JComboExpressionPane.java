    @Override
    public void parse(final Expression target) {
        if(target != null){
            if(values != null && values.contains(target)){
                guiSpecial.parse(null);
                guiCombo.setSelectedItem(target);
            }else{
                guiCombo.setSelectedItem(getFilterFactory().literal("-"));
                guiSpecial.parse(target);
            }
        }else{
            guiSpecial.parse(null);
            guiCombo.setSelectedItem(values.get(1));
        }
    }

    @Override
    public Expression create() {
        final Expression special = guiSpecial.get();
        if(special != null){
            return special;
        }else{
            return (Literal)guiCombo.getSelectedItem();
        }
    }

